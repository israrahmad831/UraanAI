<script>
    // Smooth scroll for the header indicator
    document.querySelector('.header-scroll-indicator').addEventListener('click', function(e) {
        e.preventDefault();
        document.querySelector('.section-title').scrollIntoView({
            behavior: 'smooth'
        });
    });
    
    // Initialize Bootstrap Collapse with accordion behavior
    document.addEventListener('DOMContentLoaded', function() {
        // Initialize all collapse elements
        const collapseElementList = document.querySelectorAll('.collapse');
        const collapseList = [...collapseElementList].map(collapseEl => new bootstrap.Collapse(collapseEl, {
            toggle: false
        }));
        
        // Add click event to all FAQ questions
        const faqQuestions = document.querySelectorAll('.faq-question');
        
        faqQuestions.forEach(question => {
            question.addEventListener('click', function() {
                // Get the target collapse element
                const targetId = this.getAttribute('href');
                const targetElement = document.querySelector(targetId);
                
                // Get the parent category
                const parentCategory = this.closest('.faq-category');
                
                // If this question is being expanded, collapse others in the same category
                if (!targetElement.classList.contains('show')) {
                    const openItems = parentCategory.querySelectorAll('.collapse.show');
                    openItems.forEach(item => {
                        if (item !== targetElement) {
                            const collapseInstance = bootstrap.Collapse.getInstance(item);
                            if (collapseInstance) {
                                collapseInstance.hide();
                            }
                        }
                    });
                }
            });
        });
    });
</script>