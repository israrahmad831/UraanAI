          <input type="text" class="form-control" placeholder="Role" required>
        </div>
      </div>
      <div class="row align-items-center">
        <div class="col-md-10">
          <input type="text" class="form-control" placeholder="Institution" required>
        </div>
        <div class="col-md-2">
          <button type="button" class="btn btn-sm btn-danger remove-member w-100 d-flex align-items-center justify-content-center">
            <i class="fas fa-trash"></i>
          </button>
        </div>
      </div>
    `;
    
    membersContainer.appendChild(newMember);
    memberCount++;
    
    // Add event listener to remove button
    newMember.querySelector('.remove-member').addEventListener('click', function() {
      this.closest('.team-member').remove();
      memberCount--;
    });
  });
  
  // Form submission with AJAX
// Form submission with AJAX
document.getElementById('techathon-registration').addEventListener('submit', function(e) {
    e.preventDefault();
    
    // Form validation
    const form = e.target;
    if (!form.checkValidity()) {
        form.classList.add('was-validated');
        // Scroll to first error
        const firstError = form.querySelector(':invalid');
        if (firstError) {
            firstError.scrollIntoView({ behavior: 'smooth', block: 'center' });
        }
        return;
    }
    
    // Show loading state
    const submitBtn = form.querySelector('button[type="submit"]');
    const originalBtnText = submitBtn.innerHTML;
    submitBtn.innerHTML = '<span class="spinner-border spinner-border-sm" role="status" aria-hidden="true"></span> Processing...';
    submitBtn.disabled = true;

    // Collect form data
    const formData = new FormData(form);
    
    // Add team members data
    const teamMembers = [];
    document.querySelectorAll('.team-member').forEach((member, index) => {
        const inputs = member.querySelectorAll('input');
        teamMembers.push({
            name: inputs[0].value,
            email: inputs[1].value,
            role: inputs[2].value,
            institution: inputs[3].value
        });
    });
    formData.append('teamMembers', JSON.stringify(teamMembers));
    
    // Add frameworks data
    const frameworks = [];
    document.querySelectorAll('.frameworks-container input[type="checkbox"]:checked').forEach(checkbox => {
        if (checkbox.id === 'other-framework' && document.getElementById('other-framework-text').value) {
            frameworks.push(document.getElementById('other-framework-text').value);
        } else if (checkbox.id !== 'other-framework') {
            frameworks.push(checkbox.value);
        }
    });
    formData.append('frameworks', JSON.stringify(frameworks));
    
    // AJAX submission
    fetch('https://uraanpakistan.pk/uraanai/components/submit_registration.php', {
        method: 'POST',
        body: formData
    })
    .then(response => response.json())
    .then(data => {
        if (data.success) {
            // Hide modal after successful submission
            const modal = bootstrap.Modal.getInstance(document.getElementById('registerModal'));
            modal.hide();
            
            // Show success message
            Swal.fire({
                icon: 'success',
                title: 'Registration Received!',
                html: 'Thank you for your complete registration.<br><br>' +
                      'Our team has received your project submission and will review it carefully.<br><br>' +
                      'A program administrator will contact you soon with further instructions.',
                confirmButtonColor: 'var(--accent-color)'
            });
            
            form.reset();
            form.classList.remove('was-validated');
        } else {
            // Show error message
            Swal.fire({
                icon: 'error',
                title: 'Submission Failed',
                text: data.message || 'There was an error submitting your registration. Please try again.',
                confirmButtonColor: 'var(--accent-color)'
            });
        }
    })
    .catch(error => {
        console.error('Error:', error);
        Swal.fire({
            icon: 'error',
            title: 'Submission Failed',
            text: 'There was an error submitting your registration. Please try again.',
            confirmButtonColor: 'var(--accent-color)'
        });
    })
    .finally(() => {
        submitBtn.innerHTML = originalBtnText;
        submitBtn.disabled = false;
    });
});


});
