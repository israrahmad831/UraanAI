  otherFrameworkCheckbox.addEventListener('change', function() {
    otherFrameworkText.disabled = !this.checked;
    if (!this.checked) {
      otherFrameworkText.value = '';
    }
  });
  otherFrameworkText.disabled = !otherFrameworkCheckbox.checked;
  
  // Add team member functionality
  const addMemberBtn = document.getElementById('add-member-btn');
  const membersContainer = document.getElementById('team-members-container');
  let memberCount = 1;
  
  addMemberBtn.addEventListener('click', function() {
    if (memberCount >= 3) {
      alert('Maximum of 3 team members allowed');
      return;
    }
    
    const newMember = document.createElement('div');
    newMember.className = 'team-member mb-3';
    newMember.innerHTML = `
      <div class="row">
